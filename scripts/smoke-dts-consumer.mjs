// Compiled-dist smoke: the published type surface compiles for a *default* consumer.
//
// Posthorn builds with `skipLibCheck: true`, so `tsc` never type-checks its own emitted
// `dist/**/*.d.ts` — a malformed or non-portable public declaration would ship undetected
// while the project's own strict build stays green. This smoke closes that gap: after a build
// it writes a throwaway consumer importing a representative slice of the public surface
// (signing, all three store backends incl. the Postgres exports that pull @types/pg, queue,
// worker, http, runtime, sdk, billing, metrics, net, logging) and type-checks it with a fresh,
// *non-Posthorn* tsconfig — strict (the `tsc --init` default) but without Posthorn's extra
// flags, and crucially `skipLibCheck: false` so the published `.d.ts` graph is actually checked.
//
// No gateway and no network: it only runs `tsc --noEmit`. Requires a prior `npm run build`
// (scripts/agent/smoke.sh builds first); fails clearly if `dist/index.d.ts` is missing.
import { mkdtempSync, rmSync, writeFileSync, existsSync } from "node:fs";
import { spawnSync } from "node:child_process";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

let passed = 0;
function check(label, cond) {
  if (!cond) {
    console.error(`FAIL: ${label}`);
    process.exit(1);
  }
  console.log(`✓ ${label}`);
  passed++;
}

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const distIndexJs = path.join(repoRoot, "dist", "index.js");
const distIndexDts = path.join(repoRoot, "dist", "index.d.ts");
const tscBin = path.join(repoRoot, "node_modules", "typescript", "bin", "tsc");

check("dist/index.d.ts exists (run `npm run build` first)", existsSync(distIndexDts));
check("dist/index.js exists", existsSync(distIndexJs));
check("project TypeScript is installed", existsSync(tscBin));

// A representative slice of the public surface. Values are referenced with `void` and types
// via `declare const` so the consumer never *calls* anything — that would risk false failures
// from our own usage. Importing the names is enough: with skipLibCheck off, tsc must load and
// fully type-check the published declaration graph the imports pull in.
const values = [
  "sign",
  "verify",
  "generateSecret",
  "WebhookVerificationError",
  "createGateway",
  "loadConfig",
  "runAdminCommand",
  "InMemoryMessageStore",
  "SqliteMessageStore",
  "PostgresMessageStore",
  "DeliveryWorker",
  "createApi",
  "createHttpServer",
  "buildOpenApiDocument",
  "MetricsRegistry",
  "renderPrometheus",
  "PosthornClient",
  "PosthornAdminClient",
  "verifyWebhook",
  "createPostgresPool",
  "createLogger",
  "assertUrlDeliverable",
  "MIN_ADMIN_TOKEN_LENGTH",
  "DEFAULT_STRIPE_TOLERANCE_SECONDS",
  "POSTHORN_VERSION",
];
const types = [
  "SignInput",
  "VerifyOptions",
  "GatewayConfig",
  "Gateway",
  "Endpoint",
  "Message",
  "MessageStore",
  "DeliveryQueue",
  "DeliveryFailureReason",
  "SendMessageInput",
  "EndpointView",
  "Pool",
  "PoolClient",
  "MetricsSnapshot",
  "OpenApiDocument",
  "RetryPolicy",
  "App",
  "PlanId",
  "Logger",
];

const tmpDir = mkdtempSync(path.join(os.tmpdir(), "posthorn-dts-"));
try {
  const specifier = path.relative(tmpDir, distIndexJs).replace(/\\/g, "/");
  if (!specifier.startsWith(".")) {
    // path.relative returns a drive-qualified path when tmp and repo live on different Windows
    // volumes; that is not a valid relative ESM specifier.
    console.error(
      `FAIL: temp dir (${tmpDir}) and dist (${distIndexJs}) are on different volumes; ` +
        "point TMP/TEMP at a dir on the repo's drive and retry.",
    );
    process.exit(1);
  }

  const importBlock =
    "import {\n" +
    values.map((n) => `  ${n},`).join("\n") +
    "\n" +
    types.map((n) => `  type ${n},`).join("\n") +
    `\n} from "${specifier}";\n`;
  const valueUse = values.map((n) => `void ${n};`).join("\n");
  const typeUse = types.map((n, i) => `declare const _t${i}: ${n};\nvoid _t${i};`).join("\n");

  const consumer =
    "// Auto-generated by scripts/smoke-dts-consumer.mjs — throwaway; do not edit.\n" +
    importBlock +
    "\n" +
    valueUse +
    "\n\n" +
    typeUse +
    "\n";
  writeFileSync(path.join(tmpDir, "consumer.ts"), consumer);

  // A fresh, non-Posthorn consumer config: strict (the `tsc --init` default) but without
  // Posthorn's extra flags, Node module resolution, and skipLibCheck OFF so the published
  // `.d.ts` graph is genuinely checked. typeRoots points at the repo's @types so the consumer
  // (which lives in the OS temp dir) can still resolve @types/node; the deep `pg` import inside
  // the Postgres declarations resolves relative to dist/, not the consumer.
  const typeRoots = path.join(repoRoot, "node_modules", "@types").replace(/\\/g, "/");
  const tsconfig = {
    compilerOptions: {
      module: "nodenext",
      moduleResolution: "nodenext",
      target: "es2022",
      lib: ["es2022"],
      types: ["node"],
      typeRoots: [typeRoots],
      strict: true,
      skipLibCheck: false,
      noEmit: true,
    },
    files: ["consumer.ts"],
  };
  writeFileSync(path.join(tmpDir, "tsconfig.json"), JSON.stringify(tsconfig, null, 2));

  const res = spawnSync(process.execPath, [tscBin, "-p", path.join(tmpDir, "tsconfig.json")], {
    cwd: tmpDir,
    encoding: "utf8",
  });
  if (res.error) {
    console.error(`FAIL: could not run tsc: ${res.error.message}`);
    process.exit(1);
  }
  if (res.status !== 0) {
    // tsc writes diagnostics to stdout; surface everything so a real failure is debuggable.
    if (res.stdout) process.stdout.write(res.stdout);
    if (res.stderr) process.stderr.write(res.stderr);
  }
  check(
    "published dist/index.d.ts type-checks under a default (skipLibCheck:false) consumer tsconfig",
    res.status === 0,
  );
} finally {
  rmSync(tmpDir, { recursive: true, force: true });
}

console.log(`\nDTS_CONSUMER_SMOKE_PASS ${passed}/${passed}`);
